package com.litte.entity.reception;

public class TShop {
    private String id;

    private String sSite;

    private String sPortrait;

    private String sLongitude;

    private String sLatitude;

    private String sIntroduction;

    private String remark;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getsSite() {
        return sSite;
    }

    public void setsSite(String sSite) {
        this.sSite = sSite;
    }

    public String getsPortrait() {
        return sPortrait;
    }

    public void setsPortrait(String sPortrait) {
        this.sPortrait = sPortrait;
    }

    public String getsLongitude() {
        return sLongitude;
    }

    public void setsLongitude(String sLongitude) {
        this.sLongitude = sLongitude;
    }

    public String getsLatitude() {
        return sLatitude;
    }

    public void setsLatitude(String sLatitude) {
        this.sLatitude = sLatitude;
    }

    public String getsIntroduction() {
        return sIntroduction;
    }

    public void setsIntroduction(String sIntroduction) {
        this.sIntroduction = sIntroduction;
    }

    public String getRemark() {
        return remark;
    }

    public void setRemark(String remark) {
        this.remark = remark;
    }
}